# Custom transform class to replace lambda (for multiprocessing compatibility)
class BinarizeTransform:
    def __init__(self, threshold=0.2):
        self.threshold = threshold
    
    def __call__(self, x):
        return (x > self.threshold).float()

# Better preprocessing with data augmentation for training
train_transform = transforms.Compose([
    transforms.RandomRotation(degrees=5),  # Small rotation
    transforms.RandomAffine(degrees=0, translate=(0.05, 0.05)),  # Small translation
    transforms.ToTensor(),
    BinarizeTransform(threshold=0.5),  # Lower threshold for better capture
])

# Test transform without augmentation
test_transform = transforms.Compose([
    transforms.ToTensor(),
    BinarizeTransform(threshold=0.5),
])

# Load datasets
train_set = torchvision.datasets.MNIST(root='./data', train=True, transform=train_transform, download=True)
test_set = torchvision.datasets.MNIST(root='./data', train=False, transform=test_transform, download=True)

# Split train/validation
train_size = int(0.85 * len(train_set))
val_size = len(train_set) - train_size
train_dataset, val_dataset = random_split(train_set, [train_size, val_size])

# Larger batch size with gradient accumulation (num_workers=0 for macOS compatibility)
train_loader = DataLoader(train_dataset, batch_size=256, shuffle=True, num_workers=0)
val_loader = DataLoader(val_dataset, batch_size=1000, shuffle=False, num_workers=0)
test_loader = DataLoader(test_set, batch_size=1000, shuffle=False, num_workers=0)

# Enhanced CNN with better architecture (Teacher Model)
class EnhancedCNN(nn.Module):
    def __init__(self, dropout_rate=0.3):
        super().__init__()
        # Convolutional layers
        self.conv1 = nn.Conv2d(1, 32, kernel_size=3, padding=1)  # 28x28 -> 28x28
        self.bn1 = nn.BatchNorm2d(32)
        self.conv2 = nn.Conv2d(32, 64, kernel_size=3, padding=1)  # 28x28 -> 28x28
        self.bn2 = nn.BatchNorm2d(64)
        self.conv3 = nn.Conv2d(64, 128, kernel_size=3, padding=1)  # 14x14 -> 14x14
        self.bn3 = nn.BatchNorm2d(128)
        self.conv4 = nn.Conv2d(128, 256, kernel_size=3, padding=1)  # 14x14 -> 14x14
        self.bn4 = nn.BatchNorm2d(256)
        
        # Pooling
        self.pool = nn.MaxPool2d(2, 2)
        self.adaptive_pool = nn.AdaptiveAvgPool2d((7, 7))
        
        # Fully connected layers
        self.fc1 = nn.Linear(256 * 7 * 7, 512)
        self.bn_fc1 = nn.BatchNorm1d(512)
        self.fc2 = nn.Linear(512, 256)
        self.bn_fc2 = nn.BatchNorm1d(256)
        self.fc3 = nn.Linear(256, 10)
        
        # Dropout
        self.dropout = nn.Dropout(dropout_rate)
        
        # Initialize weights
        self._initialize_weights()
    
    def _initialize_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                nn.init.kaiming_normal_(m.weight, mode='fan_out', nonlinearity='relu')
                if m.bias is not None:
                    nn.init.zeros_(m.bias)
            elif isinstance(m, nn.Linear):
                nn.init.kaiming_normal_(m.weight, mode='fan_out', nonlinearity='relu')
                nn.init.zeros_(m.bias)
            elif isinstance(m, (nn.BatchNorm2d, nn.BatchNorm1d)):
                nn.init.ones_(m.weight)
                nn.init.zeros_(m.bias)
    
    def forward(self, x):
        # Conv block 1
        x = self.conv1(x)
        x = self.bn1(x)
        x = F.relu(x)
        
        # Conv block 2
        x = self.conv2(x)
        x = self.bn2(x)
        x = F.relu(x)
        x = self.pool(x)  # 28x28 -> 14x14
        
        # Conv block 3
        x = self.conv3(x)
        x = self.bn3(x)
        x = F.relu(x)
        
        # Conv block 4
        x = self.conv4(x)
        x = self.bn4(x)
        x = F.relu(x)
        x = self.pool(x)  # 14x14 -> 7x7
        
        # Adaptive pooling to ensure consistent size
        x = self.adaptive_pool(x)  # 7x7
        
        # Flatten
        x = x.view(x.size(0), -1)
        
        # FC layers
        x = self.fc1(x)
        x = self.bn_fc1(x)
        x = F.relu(x)
        x = self.dropout(x)
        
        x = self.fc2(x)
        x = self.bn_fc2(x)
        x = F.relu(x)
        x = self.dropout(x)
        
        x = self.fc3(x)
        return x

# Simpler CNN for fixed-point export (Student Model)
class OptimizedSimpleCNN(nn.Module):
    def __init__(self):
        super().__init__()
        # Convolutional layers
        self.conv1 = nn.Conv2d(1, 16, kernel_size=5, padding=2)  # 28x28 -> 28x28
        self.bn1 = nn.BatchNorm2d(16)
        self.conv2 = nn.Conv2d(16, 32, kernel_size=5, padding=2)  # 14x14 -> 14x14
        self.bn2 = nn.BatchNorm2d(32)
        
        # Pooling
        self.pool = nn.MaxPool2d(2, 2)
        
        # Fully connected layers
        self.fc1 = nn.Linear(32 * 7 * 7, 128)
        self.bn_fc = nn.BatchNorm1d(128)
        self.dropout = nn.Dropout(0.2)
        self.fc2 = nn.Linear(128, 10)
        
        # Initialize weights
        self._initialize_weights()
    
    def _initialize_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                nn.init.kaiming_normal_(m.weight, mode='fan_out', nonlinearity='relu')
                if m.bias is not None:
                    nn.init.zeros_(m.bias)
            elif isinstance(m, nn.Linear):
                nn.init.kaiming_normal_(m.weight, mode='fan_out', nonlinearity='relu')
                nn.init.zeros_(m.bias)
            elif isinstance(m, (nn.BatchNorm2d, nn.BatchNorm1d)):
                nn.init.ones_(m.weight)
                nn.init.zeros_(m.bias)
    
    def forward(self, x):
        # Conv block 1
        x = self.conv1(x)
        x = self.bn1(x)
        x = F.relu(x)
        x = self.pool(x)  # 28x28 -> 14x14
        
        # Conv block 2
        x = self.conv2(x)
        x = self.bn2(x)
        x = F.relu(x)
        x = self.pool(x)  # 14x14 -> 7x7
        
        # Flatten
        x = x.view(x.size(0), -1)  # 32 * 7 * 7 = 1568
        
        # FC layers
        x = self.fc1(x)
        x = self.bn_fc(x)
        x = F.relu(x)
        x = self.dropout(x)
        x = self.fc2(x)
        return x

device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')

# Use enhanced model for training, then distill to simple model
teacher_model = EnhancedCNN(dropout_rate=0.25).to(device)
student_model = OptimizedSimpleCNN().to(device)

# Loss functions
criterion = nn.CrossEntropyLoss()
kl_div_loss = nn.KLDivLoss(reduction='batchmean')

# Optimizers with different learning rates
teacher_optimizer = torch.optim.AdamW(teacher_model.parameters(), lr=0.003, weight_decay=1e-4)
student_optimizer = torch.optim.AdamW(student_model.parameters(), lr=0.002, weight_decay=5e-5)

# Learning rate schedulers
teacher_scheduler = torch.optim.lr_scheduler.CosineAnnealingWarmRestarts(teacher_optimizer, T_0=10, T_mult=2)
student_scheduler = torch.optim.lr_scheduler.ReduceLROnPlateau(student_optimizer, mode='max', factor=0.5, patience=5)

# Training parameters
num_epochs = 10
best_teacher_acc = 0.0
best_student_acc = 0.0
patience_counter = 0
max_patience = 15
temperature = 4.0  # For knowledge distillation

print("Phase 1: Training Teacher CNN Model")
print("="*50)

# Phase 1: Train teacher model
for epoch in range(num_epochs):
    # Training
    teacher_model.train()
    train_loss = 0
    train_correct = 0
    train_total = 0
    
    for batch_idx, (x, y) in enumerate(train_loader):
        x, y = x.to(device), y.to(device)
        
        # Add mixup augmentation occasionally
        if np.random.random() > 0.7:
            lam = np.random.beta(1.0, 1.0)
            index = torch.randperm(x.size(0)).to(device)
            mixed_x = lam * x + (1 - lam) * x[index]
            y_a, y_b = y, y[index]
            
            logits = teacher_model(mixed_x)
            loss = lam * criterion(logits, y_a) + (1 - lam) * criterion(logits, y_b)
        else:
            logits = teacher_model(x)
            loss = criterion(logits, y)
        
        teacher_optimizer.zero_grad()
        loss.backward()
        torch.nn.utils.clip_grad_norm_(teacher_model.parameters(), max_norm=1.0)
        teacher_optimizer.step()
        
        train_loss += loss.item()
        _, predicted = torch.max(logits.data, 1)
        train_total += y.size(0)
        train_correct += (predicted == y).sum().item()
    
    teacher_scheduler.step()
    
    # Validation
    teacher_model.eval()
    val_correct = 0
    val_total = 0
    
    with torch.no_grad():
        for x, y in val_loader:
            x, y = x.to(device), y.to(device)
            logits = teacher_model(x)
            _, predicted = torch.max(logits.data, 1)
            val_total += y.size(0)
            val_correct += (predicted == y).sum().item()
    
    val_acc = val_correct / val_total
    train_acc = train_correct / train_total
    
    if val_acc > best_teacher_acc:
        best_teacher_acc = val_acc
        torch.save(teacher_model.state_dict(), './models/best_teacher_cnn_model.pth')
        patience_counter = 0
    else:
        patience_counter += 1
    
    print(f'Epoch {epoch+1}: Train Acc: {train_acc:.4f}, Val Acc: {val_acc:.4f}, Best: {best_teacher_acc:.4f}')
    
    if patience_counter >= max_patience:
        print(f"Early stopping at epoch {epoch+1}")
        break

