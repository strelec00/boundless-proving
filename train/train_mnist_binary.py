# Phase 2: Knowledge distillation to student model
patience_counter = 0
for epoch in range(10):
    student_model.train()
    train_loss = 0
    
    for x, y in train_loader:
        x, y = x.to(device), y.to(device)
        
        # Get teacher predictions
        with torch.no_grad():
            teacher_logits = teacher_model(x)
        
        # Student predictions
        student_logits = student_model(x)
        
        # Distillation loss
        soft_targets = F.softmax(teacher_logits / temperature, dim=1)
        soft_prob = F.log_softmax(student_logits / temperature, dim=1)
        distillation_loss = kl_div_loss(soft_prob, soft_targets) * (temperature ** 2)
        
        # Standard loss
        student_loss = criterion(student_logits, y)
        
        # Combined loss
        alpha = 0.7  # Weight for distillation
        loss = alpha * distillation_loss + (1 - alpha) * student_loss
        
        student_optimizer.zero_grad()
        loss.backward()
        torch.nn.utils.clip_grad_norm_(student_model.parameters(), max_norm=1.0)
        student_optimizer.step()
        
        train_loss += loss.item()
    
    # Validation
    student_model.eval()
    val_correct = 0
    val_total = 0
    
    with torch.no_grad():
        for x, y in val_loader:
            x, y = x.to(device), y.to(device)
            logits = student_model(x)
            _, predicted = torch.max(logits.data, 1)
            val_total += y.size(0)
            val_correct += (predicted == y).sum().item()
    
    val_acc = val_correct / val_total
    student_scheduler.step(val_acc)
    
    if val_acc > best_student_acc:
        best_student_acc = val_acc
        torch.save(student_model.state_dict(), './models/best_student_cnn_model.pth')
        patience_counter = 0
    else:
        patience_counter += 1
    
  
    print(f'Epoch {epoch+1}: Val Acc: {val_acc:.4f}, Best: {best_student_acc:.4f}')
    
    if patience_counter >= 15:
        print(f"Early stopping at epoch {epoch+1}")
        break

